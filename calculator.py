num1 = int(input("Enter a number "))
operator = input("Enter an operator ")
num2 = int(input("enter anothe numner "))

if operator == "+":
    print(num1 + num2)
elif operator == "-":
    print(num1 - num2)   
elif operator == "*":
    print(num1 - num2)
elif operator == "/":
    print(num1 - num2)   
elif operator == "%":
    print(num1 - num2)   
else:
    print("invalid operator")